/**
 * Represents the types of events related to message reactions.
 *
 * - `reactionsAdded`: Indicates that reactions were added to a message.
 * - `reactionsRemoved`: Indicates that reactions were removed from a message.
 */
export type MessageReactionEvents = 'reactionsAdded' | 'reactionsRemoved'
